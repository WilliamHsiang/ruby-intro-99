# Arrays are lists of things

blackpink = ["jennie", "lisa", "rose", "jisoo"]
p blackpink

# Accessing the array
puts blackpink[0]
p blackpink[-1]

# Add to the array

yg = blackpink + ["babymonster"]
p yg

yg.push "bigbang"
p yg

