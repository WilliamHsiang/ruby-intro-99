# In Ruby, there are different *types* of data:

# Numbers
# puts 3
# puts 2 + 3
# puts 2/3.0

# Non-numbers
puts "Hellow, world"

# True, False
puts true
puts false

# Nothing

# Variables
x = 3
y = 5
puts x
puts y

food = "tacos"
puts food

# Combine strings and variables
puts food + " are amazing"
puts "#{food} are amazing"

puts "#{food} are amazing " * 3

# String manipulation
puts food.upcase