# To run this code, be sure your current working directory
# is the same as where this file is located and then run:
# ruby 2-conditionals.rb

# Booleans
true
false

# Boolean Expressions
puts 3 > 2
puts 2 > 3
puts 3 == 2
puts 3 != 2

# If Conditional Logic
if 3 > 2
  puts "blackpink in your area"
end

# If/Else Conditional Logic

if false
  puts "blackpink in your area"
else
  puts "blackpink in my area"
end

# Elsif Conditional Logic

jennie = 1
jisoo = 2

if jennie > 1
  puts "blackpink in your area"
elsif jennie = jisoo
  puts "blackpink in my area"
else
  puts "blackpink everywhere"
end

# Combining Expressions

a = 1
b = 2

# and
if a == 1 && b == 2
  puts true
end

# or
if a == 1 || b == 1
  puts true
end